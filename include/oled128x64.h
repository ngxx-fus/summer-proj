#ifndef __OLED128X64_H__
#define __OLED128X64_H__

#include "i2c_com.h"

#ifndef OLED128X64_ADDR
#define OLED128X64_ADDR 0x3C
#endif 


void oled_init() {
    uint8_t init_commands[] = {
        0x0,
        0xAE, 0xD5, 0x80, 0xA8, 0x3F, 0xD3, 0x00, 0x40, 0x8D, 0x14,
        0x20, 0x00, 0xA1, 0xC8, 0xDA, 0x12, 0x81, 0xCF, 0xD9, 0xF1,
        0xDB, 0x40, 0xA4, 0xAF, 0xA5
    }; 
    send_byte_array(OLED128X64_ADDR, init_commands, sizeof(init_commands)/sizeof(uint8_t));
}

#endif